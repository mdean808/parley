import type { ComparisonReport } from "./types.ts";

export function generateMarkdownReport(report: ComparisonReport): string {
	const lines: string[] = [];
	const { protocolIds } = report;
	const metrics = report.aggregate.protocolMetrics;

	lines.push("# Protocol Interaction Benchmark Report\n");
	lines.push(`*Run: ${report.generatedAt}*\n`);

	// Summary
	lines.push("## Summary\n");
	lines.push(
		`Tested ${protocolIds.length} protocol(s) across ${report.probes.length} interaction probes using model \`${report.model}\`.\n`,
	);

	// Protocol comparison table
	lines.push("## Protocol Comparison\n");
	lines.push("| Protocol | Overall Pass | Avg Score | Avg Cost |");
	lines.push("|----------|-------------|-----------|----------|");
	for (const pid of protocolIds) {
		const m = metrics[pid];
		lines.push(
			`| ${pid} | ${m.passedCount}/${m.totalCount} (${m.overallPassRate.toFixed(1)}%) | ${m.avgInteractionScore.toFixed(1)}/3 | $${m.avgCost.toFixed(4)} |`,
		);
	}
	lines.push("");

	// Pattern breakdown
	lines.push("## Results by Pattern\n");
	const patterns = [
		"single-route",
		"selective-route",
		"decline-all",
		"handoff",
		"collaborate",
	];

	for (const pattern of patterns) {
		const hasData = protocolIds.some(
			(pid) => metrics[pid]?.byPattern[pattern]?.probeCount > 0,
		);
		if (!hasData) continue;

		lines.push(`### ${pattern}\n`);
		lines.push("| Protocol | Pass Rate | Avg Score | Probes |");
		lines.push("|----------|-----------|-----------|--------|");
		for (const pid of protocolIds) {
			const pm = metrics[pid]?.byPattern[pattern];
			if (!pm || pm.probeCount === 0) {
				lines.push(`| ${pid} | — | — | 0 |`);
			} else {
				lines.push(
					`| ${pid} | ${pm.overallPassRate.toFixed(1)}% (${pm.passedCount}/${pm.probeCount}) | ${pm.avgInteractionScore.toFixed(1)}/3 | ${pm.probeCount} |`,
				);
			}
		}
		lines.push("");
	}

	// Per-probe details
	lines.push("## Probe Details\n");
	for (const pc of report.probes) {
		lines.push(`### ${pc.probe.id}\n`);
		lines.push(
			`**Pattern:** ${pc.probe.pattern} | **Target skills:** ${pc.probe.targetSkills.join(", ") || "none"}\n`,
		);
		lines.push(`> ${pc.probe.prompt}\n`);

		lines.push("| Protocol | Overall | Assertions | Judge | Score | Agents |");
		lines.push("|----------|---------|-----------|-------|-------|--------|");
		for (const pid of protocolIds) {
			const r = pc.results[pid];
			if (!r) {
				lines.push(`| ${pid} | — | — | — | — | — |`);
				continue;
			}
			if (r.error) {
				lines.push(`| ${pid} | ERR | ERR | — | — | — |`);
				continue;
			}
			const overall = r.judge
				? r.judge.pass
					? "PASS"
					: "FAIL"
				: r.assertions.passed
					? "PASS"
					: "FAIL";
			const assertions = r.assertions.passed ? "PASS" : "FAIL";
			const judge = r.judge ? (r.judge.pass ? "PASS" : "FAIL") : "—";
			const score = r.judge ? `${r.judge.interactionScore}/3` : "—";
			const agents = r.agents.map((a) => a.agentName).join(", ") || "none";
			lines.push(
				`| ${pid} | ${overall} | ${assertions} | ${judge} | ${score} | ${agents} |`,
			);
		}
		lines.push("");
	}

	lines.push(
		"---\n*Generated by the protocol interaction benchmark system.*\n",
	);
	return lines.join("\n");
}
